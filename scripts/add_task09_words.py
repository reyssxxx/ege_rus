def main():
    with open(DATA_PATH, "r", encoding="utf-8") as f:
        data = json.load(f)

    existing = set(
        q["word_display"]
        for sc in data["subcategories"]
        for q in sc["questions"]
    )

    new_qs = []
    for display, correct, wrong, note in NEW_WORDS:
        if display not in existing:
            new_qs.append({
                "word_display": display,
                "correct_answer": correct,
                "wrong_options": wrong,
                "explanation": f"Словарное слово: {note}.",
            })

    if not new_qs:
        print("Нечего добавлять — все слова уже есть.")
        return

    data["subcategories"][0]["questions"].extend(new_qs)

    with open(DATA_PATH, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    total = sum(len(sc["questions"]) for sc in data["subcategories"])
    print(f"Добавлено: {len(new_qs)} вопросов. Итого: {total}.")


if __name__ == "__main__":
    main()
