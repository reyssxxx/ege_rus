def make_task12():
    # Clean up ambiguous entries
    verb_clean = [(w, c, wr, ex) for w, c, wr, ex in VERB_ENDINGS if wr]

    categories = [
        ("verb_ending", "Окончания глаголов",              verb_clean),
        ("part_present","Суффиксы причастий настоящего вр.", PART_PRESENT),
        ("part_past",   "Суффиксы причастий прошедшего вр.", PART_PAST),
    ]
    subcats = []
    for code, name, rows in categories:
        qs = []
        for word_disp, correct, wrong, expl in rows:
            if not wrong:
                continue
            qs.append({
                "word_display":   word_disp,
                "correct_answer": correct,
                "wrong_options":  [wrong],
                "explanation":    expl,
            })
        if qs:
            subcats.append({"code": code, "name": name, "rule": "", "questions": qs})

    data = {
        "task_number": 12,
        "task_name":   "Правописание глагольных форм",
        "subcategories": subcats,
    }
    path = os.path.join(DATA, "task_12_verb_endings.json")
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    total = sum(len(s["questions"]) for s in subcats)
    print(f"task_12_verb_endings.json: {total} questions")


