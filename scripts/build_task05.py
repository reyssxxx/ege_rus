def _to_display_case(word: str) -> str:
    """Convert ALL-CAPS word to Title Case for button display."""
    return word.capitalize()


def _make_gap_sentence(sentence: str, wrong_caps: str) -> str:
    """Replace the all-caps Russian word token in sentence with _____ gap."""
    import re
    # Find any token of 2+ consecutive uppercase Cyrillic letters (the highlighted wrong word)
    result = re.sub(r'[А-ЯЁ]{2,}(?:-[А-ЯЁ]+)*', "_____", sentence, count=1)
    return result


